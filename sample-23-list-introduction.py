# List is a datatype which can have multiple values in an ordered sequence.
# Values within a list are items.

batters = ["Sachin", "Virat", "Lara", "Gilchrist", "Kallis"]
batting_averages = [42.5, 52.4, 40.3, 38.6, 41.6]

for batter in batters:
    print(batter)

for avg in batting_averages:
    print(avg)
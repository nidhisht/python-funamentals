# Example 1: Assigning values to variables
x = 10          # Integer
y = 3.14        # Float
name = "Alice"  # String
is_active = True # Boolean

print(x)        # Output: 10
print(y)        # Output: 3.14
print(name)     # Output: Alice
print(is_active) # Output: True

# Example 2: Multiple assignments
a, b, c = 1, 2, 3
print(a, b, c)  # Output: 1 2 3

# Example 3: Swapping values
x, y = y, x
print(x, y)     # Output: 3.14 10

# Example 4: Constants (by convention, use uppercase names)
PI = 3.14159
GRAVITY = 9.8
print(PI, GRAVITY)  # Output: 3.14159 9.8


# Example 5: Variable types can change
variable = 42         # Initially an integer
print(variable)       # Output: 42
variable = "Now a string"
print(variable)       # Output: Now a string
